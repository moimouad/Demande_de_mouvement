package com.octest.beans;

public class Demande {
	private int id;
	private int id_source;
	private String date_demande;
	private String type;
	private String etat;
	private String date;
	private String heure;
	private String poste_act;
	private String poste_dem;
	private int id_perm;
	private String motif;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getId_source() {
		return id_source;
	}
	public void setId_source(int id_source) {
		this.id_source = id_source;
	}
	public String getDate_demande() {
		return date_demande;
	}
	public void setDate_demande(String date_demande) {
		this.date_demande = date_demande;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getEtat() {
		return etat;
	}
	public void setEtat(String etat) {
		this.etat = etat;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getHeure() {
		return heure;
	}
	public void setHeure(String heure) {
		this.heure = heure;
	}
	public String getPoste_act() {
		return poste_act;
	}
	public void setPoste_act(String poste_act) {
		this.poste_act = poste_act;
	}
	public String getPoste_dem() {
		return poste_dem;
	}
	public void setPoste_dem(String poste_dem) {
		this.poste_dem = poste_dem;
	}
	public int getId_perm() {
		return id_perm;
	}
	public void setId_perm(int id_perm) {
		this.id_perm = id_perm;
	}
	public String getMotif() {
		return motif;
	}
	public void setMotif(String motif) {
		this.motif = motif;
	}
	
	

}
